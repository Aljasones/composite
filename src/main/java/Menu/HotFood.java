package Menu;

public class HotFood implements Menu {
    private String name;
    private String price;
    private String weight;
    private String calories;

    public String getName() {
        return name;
    }
    public String getPrice() {
        return price;
    }
    public String getWeight() {
        return weight;
    }

    public String getCalories() {
        return calories;
    }

}
